import mongoose from 'mongoose';

const recipeSchema = mongoose.Schema({
    title: String,
    message: String,
    name: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likes: { type: [String], default: [] },
    comments: { type: [String], default: [] },
    createdAt: {
        type: Date,
        default: new Date(),
    },
})

var recipeMessage = mongoose.model('Recipee', recipeSchema);

export default recipeMessage;